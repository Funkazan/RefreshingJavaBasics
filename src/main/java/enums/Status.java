package enums;

/**
 * Represents the possible statuses of a task or process.
 * 
 *   {@link #TODO} - The task has not been started yet.
 *   {@link #IN_PROGRESS} - The task is currently being worked on.
 *   {@link #DONE} - The task has been completed.
 * 
 */
public enum Status {
    TODO,
    IN_PROGRESS,
    DONE;
}
